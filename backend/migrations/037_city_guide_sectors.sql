-- Per-place extras on the existing directory table. The table uses the
-- _ar/_sorani/_badini suffix convention (not _ckb/_kmr), so opening hours
-- follow it for consistency with name_/description_.
ALTER TABLE city_directory_entries
  ADD COLUMN IF NOT EXISTS sectors               TEXT[] NOT NULL DEFAULT '{}',
  ADD COLUMN IF NOT EXISTS opening_hours         TEXT,
  ADD COLUMN IF NOT EXISTS opening_hours_ar      TEXT,
  ADD COLUMN IF NOT EXISTS opening_hours_sorani  TEXT,
  ADD COLUMN IF NOT EXISTS opening_hours_badini  TEXT,
  ADD COLUMN IF NOT EXISTS gallery               TEXT[] NOT NULL DEFAULT '{}';

-- GIN index so a sector-filtered City Guide query (sectors @> ARRAY[slug])
-- stays fast as the directory grows.
CREATE INDEX IF NOT EXISTS idx_city_directory_sectors ON city_directory_entries USING GIN (sectors);
