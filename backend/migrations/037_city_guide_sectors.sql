-- 037 — City Guide sectors + hours + gallery (#29).
--
-- Two parts:
--   1. city_sectors — an admin-managed, ordered, 4-language taxonomy the City
--      Guide is organised by (filter chips in the app). Mirrors the
--      project_categories / media_categories CMS pattern (slug + name_en/ar/
--      ckb/kmr + display_order + active). Seeded with 6 starter sectors,
--      admin-editable afterwards.
--   2. Extend city_directory_entries with the per-place extras #29 adds:
--      sectors (which sectors a place belongs to), 4-language opening hours,
--      and a photo gallery (reuses the #23 gallery TEXT[] convention).
--
-- Idempotent: CREATE ... IF NOT EXISTS, seed ON CONFLICT DO NOTHING, and
-- ADD COLUMN IF NOT EXISTS.

CREATE TABLE IF NOT EXISTS city_sectors (
  id            INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  slug          VARCHAR(64)  NOT NULL UNIQUE,
  name_en       VARCHAR(120) NOT NULL,
  name_ar       VARCHAR(120) NOT NULL DEFAULT '',
  name_ckb      VARCHAR(120) NOT NULL DEFAULT '',
  name_kmr      VARCHAR(120) NOT NULL DEFAULT '',
  display_order INTEGER      NOT NULL DEFAULT 0,
  active        SMALLINT     NOT NULL DEFAULT 1,
  created_by    INTEGER,
  created_at    TIMESTAMP    NOT NULL DEFAULT CURRENT_TIMESTAMP
);

