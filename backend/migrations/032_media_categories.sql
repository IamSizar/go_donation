-- Tag column on posts. Nullable (a post may be uncategorized); free VARCHAR so a
-- category delete never breaks existing posts (they keep the slug text, just
-- drop out of the filter chips). Indexed for the per-category feed filter.
ALTER TABLE media_posts
  ADD COLUMN IF NOT EXISTS category_slug VARCHAR(64);

CREATE INDEX IF NOT EXISTS idx_media_posts_category
  ON media_posts (category_slug)
  WHERE category_slug IS NOT NULL;
