-- 032 — "Our Work" media-category CMS (#22).
--
-- An admin-managed, ordered, 4-language taxonomy for the News & Activities
-- ("Our Work") feed. Each media post can be tagged with one category_slug; the
-- app shows category filter chips and a category label per post. Mirrors the
-- project_categories CMS (029): name_en/ar/ckb/kmr + display_order + active.
CREATE TABLE IF NOT EXISTS media_categories (
  id            INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  slug          VARCHAR(64)  NOT NULL UNIQUE,
  name_en       VARCHAR(120) NOT NULL,
  name_ar       VARCHAR(120) NOT NULL DEFAULT '',
  name_ckb      VARCHAR(120) NOT NULL DEFAULT '',
  name_kmr      VARCHAR(120) NOT NULL DEFAULT '',
  display_order INTEGER      NOT NULL DEFAULT 0,
  active        SMALLINT     NOT NULL DEFAULT 1,
  created_by    INTEGER,
  created_at    TIMESTAMP    NOT NULL DEFAULT CURRENT_TIMESTAMP
);

